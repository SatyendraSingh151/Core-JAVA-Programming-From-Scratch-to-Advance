package wrapping_Class;
//unboxing - > the process is to convert into primitive data from non primitive data

public class Unboxing {

	public static void main(String[] args) {
		
		Integer rNo=151;
		int i1 = rNo;
		System.out.println(i1);
		
		Boolean b = true;
		boolean b1= b;
		System.out.println(b1);
		
		Byte x = 1;
		byte x1=x;
		System.out.println(x1);
		
	
		

	}

}
