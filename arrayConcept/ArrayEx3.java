package arrayConcept;

public class ArrayEx3 {

	public static void main(String[] args) {
		// initialization
		System.out.println("Decleration of array is done.\n");
		String fruits[] = new String[5];
		fruits[0]="Apple";
		System.out.println("\nInitialization of array is done.\n");
		fruits[1]="Mango";
		fruits[2]="Lichi";
		fruits[3]="Guava";
		fruits[4]="Pineapple";
		
		
		System.out.println("\nFetching the array elements is done here\n");
		
		System.out.println(fruits[0]);
		System.out.println(fruits[1]);
		System.out.println(fruits[2]);
		System.out.println(fruits[3]);
		System.out.println(fruits[4]);
	}

}
