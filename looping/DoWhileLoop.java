package looping;

public class DoWhileLoop {

	public static void main(String[] args) {
		//this is the do while loop
		
		int n = 41,i = 1;
		do {
			System.out.println(n + " * "+i+" = "+n*i);
			i++;
		}while(i<=10);
		System.out.println("\nThe loop has now ended");
//		printing 1 to 10
		
		int x=1;
		do {
			System.out.println(x);
			x++;
		}while(x<=10);
		
		

	}

}
