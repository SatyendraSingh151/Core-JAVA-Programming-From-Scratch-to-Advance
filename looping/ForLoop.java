package looping;

public class ForLoop {

	public static void main(String[] args) {
//		printing name 
		System.out.println("to print he name for 10 times");
		for(int i=1;i<=10;i++) {
			System.out.println(i+" Satyendra Singh");
		}
		
		System.out.println("\n");
//		counting
		
		System.out.println("counting upto 10 ");
		for(int i=1;i<=10;i++) {
			System.out.println("counting "+i);
		}
		
//		multiple of n
		
		System.out.println("Table ");
		for(int i=1;i<=10;i++) {
			System.out.println(11*i);
		}
		
		
		
		int dig=8;
		for(int i=1; i<=10;i++) {
			
			System.out.println(dig +"*"+i +" = "+dig*i);
		}

	}

}
